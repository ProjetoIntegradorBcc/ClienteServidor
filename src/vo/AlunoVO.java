package vo;

public class AlunoVO {
    
    private String codigo;
    private String ra;
    private String nome;
    private String idade;
    private String endereco;
    private String curso;
    private String anoDeEntrada;
    private String disciplinasMatriculadas;
    private String disciplinasConcluidas;

    /**
     * @return the codigo
     */
    public String getCodigo() {
        return codigo;
    }

    /**
     * @param codigo the codigo to set
     */
    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    /**
     * @return the ra
     */
    public String getRa() {
        return ra;
    }

    /**
     * @param ra the ra to set
     */
    
    public void setRa(String ra) {
        this.ra = ra;
    }

    /**
     * @return the nome
     */
    
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the idade
     */
    
    public String getIdade() {
        return idade;
    }

    /**
     * @param idade the idade to set
     */
    
    public void setIdade(String idade) {
        this.idade = idade;
    }

    /**
     * @return the endereco
     */
    
    public String getEndereco() {
        return endereco;
    }

    /**
     * @param endereco the endereco to set
     */
    
    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    /**
     * @return the curso
     */
    
    public String getCurso() {
        return curso;
    }

    /**
     * @param curso the curso to set
     */
    
    public void setCurso(String curso) {
        this.curso = curso;
    }

    /**
     * @return the anoDeEntrada
     */
    
    public String getAnoDeEntrada() {
        return anoDeEntrada;
    }

    /**
     * @param anoDeEntrada the anoDeEntrada to set
     */
    
    public void setAnoDeEntrada(String anoDeEntrada) {
        this.anoDeEntrada = anoDeEntrada;
    }

    /**
     * @return the disciplinasMatriculadas
     */
    
    public String getDisciplinasMatriculadas() {
        return disciplinasMatriculadas;
    }

    /**
     * @param disciplinasMatriculadas the disciplinasMatriculadas to set
     */
    
    public void setDisciplinasMatriculadas(String disciplinasMatriculadas) {
        this.disciplinasMatriculadas = disciplinasMatriculadas;
    }

    /**
     * @return the disciplinasConcluidas
     */
    
    public String getDisciplinasConcluidas() {
        return disciplinasConcluidas;
    }

    /**
     * @param disciplinasConcluidas the disciplinasConcluidas to set
     */
    
    public void setDisciplinasConcluidas(String disciplinasConcluidas) {
        this.disciplinasConcluidas = disciplinasConcluidas;
    }
}
